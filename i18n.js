export function t(lang, key, variables = {}) {
  const normalized = normalizeUiLanguage(lang);
  const table = MESSAGES[normalized] || MESSAGES[DEFAULT_SETTINGS.uiLanguage];
  const fallback = MESSAGES.en;
  const template = table[key] ?? fallback[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_match, name) => {
    return variables[name] == null ? "" : String(variables[name]);
  });
}

export async function getStoredUiLanguage() {
  const { settings } = await chrome.storage.local.get("settings");
  return normalizeUiLanguage(settings?.uiLanguage);
}

export function setDocumentLanguage(lang) {
  document.documentElement.lang = normalizeUiLanguage(lang);
}
